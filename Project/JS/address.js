function findAddress(){
    if(document.getElementById("select").value == -1){
        mapArr = map
    }else{
        mapArr = map.filter((element)=>{
            if(element.id == document.getElementById("select").value){
                return element
            }
        })
    }
    renderMapa()
}

renderMapa()
function renderMapa(){
    let text=""
    for(let i=0;i<mapArr.length;i++){
        text += 
        `
            <div class="renderMap__address">
                <h2>${mapArr[i].city}</h2>
                <div class="render__map">
                <p>111</p>
                </div>
            </div>
        `
    }
    document.getElementById("renderMap").innerHTML = text
    for(let j=0;j<mapArr.length;j++){
        let address =""
        for (let i = 0; i < mapArr[j].address.length; i++) {
            address += 
            `
                <p onclick="checkMap(${j},${i})">${mapArr[j].address[i].name}</p>
            `    
        }
        document.getElementsByClassName("render__map")[j].innerHTML = address
    }
}
function checkMap(a,b){ 
    document.getElementById("map").innerHTML=mapArr[a].address[b].mapAddress
}